
function equality(a,b){
   if (a>b){
    console.log("😍 the first number is larger");
   }
  else if (a<b){
    console.log("💕 the second number is larger");
   }
   else if (a==b){
    console.log("❤️ both numbers are equal");
   }
}
equality(10,10);
equality(10,70);
equality(100,10);
