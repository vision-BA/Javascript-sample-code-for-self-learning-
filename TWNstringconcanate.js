//using template literals
var firstName="John";
var lastName="Doe";
var fullName=`${firstName} ${lastName}`;
console.log(fullName); // Output: "John Doe"

var a="Hello, ";
var b="World!";
var c=`${a}${b}`;
console.log(c); // Output: "Hello, World!"

